SYSTEM_PROMPT_TOPIC = """Вы классифицируете HR-запрос по теме. Возможные темы: «приём», «увольнение», «отпуска», «дисциплина», «оплата», «охрана_труда», «социальные_гарантии», «обучение», «договоры», «общее». Верните строго JSON: {"topics": ["...", "..."]} — до 3 наиболее подходящих тем. Никакого текста вне JSON."""


def build_rag_prompt(question: str, chunks: list[dict]) -> str:
    """Формирует пользовательскую часть промпта с цитатами."""
    if not chunks:
        return (
            f"Вопрос: {question}\n\n"
            "(База знаний не содержит релевантных фрагментов.)\n"
            "Ответ: В предоставленных выдержках нет информации по этому вопросу. "
            "Рекомендую обратиться к официальному источнику или уточнить запрос."
        )
    citations = []
    for i, c in enumerate(chunks, start=1):
        title = c.get("title") or c.get("source_uri") or "Источник"
        citations.append(f"[{i}] {title}\n{c.get('text','').strip()}")
    body = "\n\n".join(citations)
    return f"""Ответьте на вопрос HR-специалиста СТРОГО по выдержкам ниже. Запрещено добавлять номера статей, цитаты или нормы, которых в выдержках нет. Цитировать текст, который ЕСТЬ в выдержках, можно и нужно, если об этом просят. Если выдержки не содержат прямого ответа — так и напишите.

Выдержки:
{body}

Вопрос: {question}

Ответ (только по выдержкам выше; каждое утверждение из выдержки помечайте ссылкой [номер выдержки], например: «…срок составляет три дня [1].»):"""
