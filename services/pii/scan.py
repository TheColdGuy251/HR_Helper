"""А8: эвристический детектор персональных данных в документе для ОБЩЕЙ базы
знаний. Полностью детерминированный (без LLM) — вызывается при индексации.

В общей БЗ не должно быть ПДн (протокол, п.«RAG»); для файлов с ПДн есть
шифрованный раздел «Персональные данные». Нормативные акты легитимно содержат
несколько ФИО (подписанты) — поэтому предупреждаем по СОВОКУПНОСТИ сигналов,
а не по первому совпадению.
"""

from __future__ import annotations

import re

# Полное ФИО: третье слово — отчество (…вич/…вна/…ична/…ичны). Требование
# отчества отсекает случайные тройки заглавных слов («Российской Федерации …»).
_FIO_FULL_RE = re.compile(
    r"\b([А-ЯЁ][а-яё]{2,})\s+([А-ЯЁ][а-яё]{2,})\s+"
    r"([А-ЯЁ][а-яё]+(?:вич|вна|ична|ичны|оглы|кызы))\b"
)
# «Иванов И.И.» / «И.И. Иванов»
_FIO_SHORT_RE = re.compile(
    r"\b[А-ЯЁ][а-яё]{2,}\s+[А-ЯЁ]\.\s?[А-ЯЁ]\.|\b[А-ЯЁ]\.\s?[А-ЯЁ]\.\s+[А-ЯЁ][а-яё]{2,}\b"
)

# Маркеры критичных идентификаторов (достаточно одного класса + любых ФИО)
_ID_MARKERS: list[tuple[str, re.Pattern]] = [
    ("СНИЛС", re.compile(r"\bСНИЛС\b|\b\d{3}-\d{3}-\d{3}[- ]\d{2}\b", re.IGNORECASE)),
    ("паспорт", re.compile(r"паспорт\w*\s+(серия|сери\w|№|номер)|серия\s+\d{4}\s+№?\s*\d{6}", re.IGNORECASE)),
    ("дата рождения", re.compile(r"дата\s+рождени|г\.\s?р\.\s*\d{2}\.\d{2}\.\d{4}|\bродил[ас]", re.IGNORECASE)),
    ("табельный номер", re.compile(r"табельн\w+\s+номер", re.IGNORECASE)),
    ("личное дело", re.compile(r"личн\w+\s+дел\w+\s+№", re.IGNORECASE)),
]

# Порог «похоже на список сотрудников»: столько РАЗНЫХ полных ФИО в тексте
# у нормативного акта не бывает (подписанты — 1–3).
_FIO_LIST_THRESHOLD = 6


def scan_pii_signals(text: str) -> dict | None:
    """Возвращает описание найденных признаков ПДн или None, если документ чист.

    Предупреждение срабатывает, если:
      - разных полных ФИО ≥ _FIO_LIST_THRESHOLD (похоже на список людей), ИЛИ
      - есть полное ФИО И критичный идентификатор (СНИЛС/паспорт/дата рождения…).
    """
    if not text:
        return None
    sample = text[:500_000]  # достаточно и дёшево

    fios = {" ".join(m.groups()) for m in _FIO_FULL_RE.finditer(sample)}
    markers = [name for name, rx in _ID_MARKERS if rx.search(sample)]

    listy = len(fios) >= _FIO_LIST_THRESHOLD
    combo = bool(fios) and bool(markers)
    if not (listy or combo):
        return None

    # Образцы маскируем: «Иванов И.» — достаточно, чтобы редактор понял, о ком речь.
    def _mask(fio: str) -> str:
        parts = fio.split()
        return f"{parts[0]} {parts[1][:1]}." if len(parts) > 1 else parts[0]

    return {
        "fio_count": len(fios),
        "markers": markers,
        "samples": sorted(_mask(f) for f in list(fios)[:3]),
        "reason": "много ФИО (похоже на список сотрудников)" if listy
        else f"ФИО рядом с идентификаторами: {', '.join(markers)}",
    }
