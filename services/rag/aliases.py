def _re_for_abbr(abbr: str) -> re.Pattern:
    # \b границы для коротких аббревиатур, чтобы не задевать слова
    return re.compile(rf"(?<![А-Яа-яA-Za-z]){re.escape(abbr)}(?![А-Яа-яA-Za-z])")


# Сортируем по убыванию длины — «ТК РФ» проверяем раньше «ТК», иначе короткая
# аббревиатура съест начало длинной.
_PATTERNS = [
    (_re_for_abbr(a), a, full)
    for a, full in sorted(ABBREVIATIONS.items(), key=lambda kv: -len(kv[0]))
]


def expand_abbreviations(text: str) -> str:
    """Inline-расширение: «… ТК …» → «… ТК (Трудовой кодекс) …».
    Сохраняем оригинальную форму — BM25 от этого только выигрывает (слово остаётся),
    а dense получает доп. семантику в скобках.
    """
    if not text:
        return text
    out = text
    for pat, abbr, full in _PATTERNS:
        # Если полная форма уже рядом — пропускаем, чтобы не плодить «ТК (... ) (... )»
        if full.lower() in out.lower():
            continue
        out = pat.sub(f"{abbr} ({full})", out, count=1)
    return out


