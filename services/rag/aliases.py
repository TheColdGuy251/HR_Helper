def expand_synonyms(text: str) -> str:
    """Дописывает к запросу юридические термины по разговорным триггерам.
    Пример: «сотрудник заболел, что делать» →
    «сотрудник заболел, что делать [временная нетрудоспособность пособие …]».
    """
    if not text:
        return text
    low = text.lower()
    additions: list[str] = []
    for pat, legal in _SYN_PATTERNS:
        if pat.search(low) and legal.lower() not in low:
            additions.append(legal)
    if not additions:
        return text
    return f"{text} [{' ; '.join(additions)}]"
